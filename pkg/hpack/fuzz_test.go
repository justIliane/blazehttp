package hpack

import "testing"

func FuzzHPACKDecode(f *testing.F) {
	// Seeds from RFC examples.
	f.Add([]byte{0x82, 0x86, 0x84, 0x41, 0x0f, 0x77, 0x77, 0x77, 0x2e, 0x65, 0x78, 0x61, 0x6d, 0x70, 0x6c, 0x65, 0x2e, 0x63, 0x6f, 0x6d})
	f.Add([]byte{0x82}) // indexed :method GET
	f.Add([]byte{0x40, 0x0a, 0x63, 0x75, 0x73, 0x74, 0x6f, 0x6d, 0x2d, 0x6b, 0x65, 0x79, 0x0d, 0x63, 0x75, 0x73, 0x74, 0x6f, 0x6d, 0x2d, 0x68, 0x65, 0x61, 0x64, 0x65, 0x72})
	f.Add([]byte{0x20, 0x00}) // dynamic table size update to 0
	f.Add([]byte{0x10, 0x08, 0x70, 0x61, 0x73, 0x73, 0x77, 0x6f, 0x72, 0x64, 0x06, 0x73, 0x65, 0x63, 0x72, 0x65, 0x74}) // never indexed

	// Malformed seeds.
	f.Add([]byte{})
	f.Add([]byte{0x00})
	f.Add([]byte{0xff, 0xff, 0xff})
	f.Add([]byte{0x80}) // index 0 (invalid)
	f.Add([]byte{0x41, 0x8a, 0x08, 0x9d, 0x5c, 0x0b, 0x81, 0x70, 0xdc, 0x78, 0x0f, 0x03}) // Huffman encoded

	f.Fuzz(func(t *testing.T, data []byte) {
		dec := AcquireDecoder()
		defer ReleaseDecoder(dec)
		// Must never panic.
		_, _ = dec.Decode(data)
	})
}

func FuzzHuffmanDecode(f *testing.F) {
	f.Add(huffmanEncode(nil, []byte("www.example.com")))
	f.Add(huffmanEncode(nil, []byte("no-cache")))
	f.Add(huffmanEncode(nil, []byte("")))
	f.Add([]byte{0xff})
	f.Add([]byte{0x00})
	f.Add([]byte{})

	f.Fuzz(func(t *testing.T, data []byte) {
		// Must never panic.
		_, _ = huffmanDecode(nil, data)
	})
}
